#include "board_painter.h"
#include <stdio.h>

void
print_horizontal_line(void)
{
	puts("+---+---+---+---+---+---+---+---+");
}

void
print_free_spaces(void)
{
	puts("|   |   |   |   |   |   |   |   |");
}

/*
  Draws the board. This function ought to be only called once
  at the start of the program.
 */
void
bp_print_board(void)
{
	print_horizontal_line();
	for (int i = 0; i < 8; ++i)
	{
		print_free_spaces();
		print_horizontal_line();
	}
}
